std::vector<std::pair<JSTokenKind, std::string>> tokenize(JSLexer &lexer) {
  std::vector<std::pair<JSTokenKind, std::string>> values;
  JSTokenKind kind = JSTokenKind::INVALID;
  do {
    Token token;
    kind = lexer.nextToken(token);
    values.emplace_back(kind, lexer.toTokenText(token));
  } while (kind != JSTokenKind::EOS);
  return values;
}

class JSLexerTest : public ::testing::Test {
public:
  static void checkString(const char *literal, const std::u16string &expected) {
    std::string err;
    auto ret = JSLexer::unquoteString(literal, &err);
    ASSERT_TRUE(ret.has_value());
    ASSERT_TRUE(err.empty());
    ASSERT_EQ(expected, ret.value());
  }

  static void checkStringErr(const char *literal, const std::string &expectedErr) {
    std::string err;
    auto ret = JSLexer::unquoteString(literal, &err);
    ASSERT_FALSE(ret.has_value());
    ASSERT_FALSE(err.empty());
    ASSERT_EQ(expectedErr, err);
  }
};


using TokenList = std::vector<std::pair<JSTokenKind, std::string>>;

static std::pair<TokenList, TokenList> split(const TokenList &list) {
  TokenList sub1;
  TokenList sub2;
  unsigned int half = list.size() / 2;
  for (unsigned int i = 0; i < half; i++) {
    sub1.emplace_back(list[i]);
  }
  for (unsigned int i = half; i < list.size(); i++) {
    sub2.emplace_back(list[i]);
  }
  return {std::move(sub1), std::move(sub2)};
}


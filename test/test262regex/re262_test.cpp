TEST(MetaDataTest, error) {
  const char *input = "";
  std::string err;
  auto ret = TestMetaData::extractFrom(input, &err);
  ASSERT_FALSE(ret.has_value());
  ASSERT_EQ("cannot extract meta data section", err);

  err.clear();
  input = "/*---*/";
  ret = TestMetaData::extractFrom(input, &err);
  ASSERT_FALSE(ret.has_value());
  ASSERT_EQ("cannot extract meta data section", err);

  err.clear();
  input = "/*------*/";
  ret = TestMetaData::extractFrom(input, &err);
  ASSERT_FALSE(ret.has_value());
  ASSERT_EQ("cannot extract meta data section", err);

  err.clear();
  input = R"(/*---
#
 fhaurf
---*/)";
  ret = TestMetaData::extractFrom(input, &err);
  ASSERT_FALSE(ret.has_value());
  ASSERT_EQ("unrecognized line in meta-data section\nfhaurf", err);

  err.clear();
  input = R"(/*---
hoge: hello world
---*/)";
  ret = TestMetaData::extractFrom(input, &err);
  ASSERT_FALSE(ret.has_value());
  ASSERT_EQ("unrecognized attribute: hoge\nhoge: hello world", err);

  err.clear();
  input = R"(/*---
author: [1234]
---*/)";
  ret = TestMetaData::extractFrom(input, &err);
  ASSERT_FALSE(ret.has_value());
  ASSERT_EQ("unrecognized attribute: author\nauthor: [1234]", err);

  err.clear();
  input = R"(/*---
features: >
[hello]
---*/)";
  ret = TestMetaData::extractFrom(input, &err);
  ASSERT_FALSE(ret.has_value());
  ASSERT_EQ("unrecognized attribute: features\nfeatures: >", err);

  err.clear();
  input = R"(/*---
includes: |
[hello]
---*/)";
  ret = TestMetaData::extractFrom(input, &err);
  ASSERT_FALSE(ret.has_value());
  ASSERT_EQ("unrecognized attribute: includes\nincludes: |", err);

  err.clear();
  input = R"(/*---
negative: |
this is
---*/)";
  ret = TestMetaData::extractFrom(input, &err);
  ASSERT_FALSE(ret.has_value());
  ASSERT_EQ("unrecognized attribute: negative\nnegative: |", err);

  err.clear();
  input = R"(/*---
negative:
phase: runtime
type: error
---*/)";
  ret = TestMetaData::extractFrom(input, &err);
  ASSERT_FALSE(ret.has_value());
  ASSERT_EQ("attribute must start with indent in negative:\nphase: runtime", err);

  err.clear();
  input = R"(/*---
negative:
 phase: runtime
type: error
---*/)";
  ret = TestMetaData::extractFrom(input, &err);
  ASSERT_FALSE(ret.has_value());
  ASSERT_EQ("attribute must start with indent in negative:\ntype: error", err);

  err.clear();
  input = R"(/*---
negative:
 phase: runtime
 phase
---*/)";
  ret = TestMetaData::extractFrom(input, &err);
  ASSERT_FALSE(ret.has_value());
  ASSERT_EQ("unrecognized attribute in negative:\nphase", err);

  err.clear();
  input = R"(/*---
negative:
 phase: runtimed
 type: error
---*/)";
  ret = TestMetaData::extractFrom(input, &err);
  ASSERT_FALSE(ret.has_value());
  ASSERT_EQ("unrecognized value in phase: runtimed, must be `parse' or `runtime'", err);

  err.clear();
  input = R"(/*---
negative:
 phase: runtime
 Type: error
---*/)";
  ret = TestMetaData::extractFrom(input, &err);
  ASSERT_FALSE(ret.has_value());
  ASSERT_EQ("unrecognized attribute in negative:\nType: error", err);

  err.clear();
  input = R"(/*---
author: my
negative:
---*/)";
  ret = TestMetaData::extractFrom(input, &err);
  ASSERT_FALSE(ret.has_value());
  ASSERT_EQ("phase or type are not provided", err);

  err.clear();
  input = R"(/*---
author: my
negative:
  phase: parse
---*/)";
  ret = TestMetaData::extractFrom(input, &err);
  ASSERT_FALSE(ret.has_value());
  ASSERT_EQ("phase or type are not provided", err);

  err.clear();
  input = R"(/*---
author: my
negative:
    type: parse error
---*/)";
  ret = TestMetaData::extractFrom(input, &err);
  ASSERT_FALSE(ret.has_value());
  ASSERT_EQ("phase or type are not provided", err);

  err.clear();
  input = R"(/*---
author: my
flags: 1234
---*/)";
  ret = TestMetaData::extractFrom(input, &err);
  ASSERT_FALSE(ret.has_value());
  ASSERT_EQ("unrecognized attribute: flags\nflags: 1234", err);

  err.clear();
  input = R"(/*---
author: my
flags: [generated, huga]
---*/)";
  ret = TestMetaData::extractFrom(input, &err);
  ASSERT_FALSE(ret.has_value());
  ASSERT_EQ("unrecognized value in flags: huga, must be `onlyStrict', `noStrict' or `generated'",
            err);
}

